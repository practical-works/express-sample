//==============================================================================
// ■ Hot-Reloader (hot-reloader.js)
//------------------------------------------------------------------------------
//     Auto refresh browser.
//==============================================================================

//------------------------------------------------------------------------------
// ► Exports
//------------------------------------------------------------------------------
module.exports = require("connect-livereload")();

//------------------------------------------------------------------------------
// ● Server
//------------------------------------------------------------------------------
const liveReloadServer = require("livereload").createServer();
liveReloadServer.server.once("connection", () => {
  setTimeout(() => {
    liveReloadServer.refresh("/");
    console.log("Reloaded".bgInfo, "Browser page refreshed.");
  }, 100);
});

