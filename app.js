//==============================================================================
// ■ App (app.js)
//------------------------------------------------------------------------------
//     Application main entry point.
//==============================================================================
require("./utils");
const app = require("express")();

//------------------------------------------------------------------------------
// ► Exports
//------------------------------------------------------------------------------
module.exports = app;

//------------------------------------------------------------------------------
// ● Settings
//------------------------------------------------------------------------------
app.set("host", process.env.HOST);
app.set("port", process.env.PORT);

//------------------------------------------------------------------------------
// ● Middlewares
//------------------------------------------------------------------------------
const middlewares = require("./middlewares");
app.use(middlewares);

//------------------------------------------------------------------------------
// ● Router
//------------------------------------------------------------------------------
const router = require("./router");
app.use(router);