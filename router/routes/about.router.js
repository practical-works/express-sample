//==============================================================================
// ■ About-Router (about.router.js)
//------------------------------------------------------------------------------
//     About page routes entry point.
//==============================================================================
const aboutRouter = require("express").Router();

//------------------------------------------------------------------------------
// ► Exports
//------------------------------------------------------------------------------
module.exports = aboutRouter;

//------------------------------------------------------------------------------
// ● GET-About-Page
//------------------------------------------------------------------------------
aboutRouter.get("/", (req, res, next) => {
  try {
    res.render("./", { title: "About", page: "about" });
  } catch (e) {
    next(e);
  }
});
